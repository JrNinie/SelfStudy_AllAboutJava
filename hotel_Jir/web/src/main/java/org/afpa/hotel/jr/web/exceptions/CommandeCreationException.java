package org.afpa.hotel.jr.web.exceptions;

public class CommandeCreationException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7051109453924157859L;

	public CommandeCreationException() {
		super();
	}

	public CommandeCreationException(String message, Throwable cause) {
		super(message, cause);
	}

	public CommandeCreationException(String message) {
		super(message);
	}

	public CommandeCreationException(Throwable cause) {
		super(cause);
	}

	
}
